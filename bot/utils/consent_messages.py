    "Для использования сервиса ИТ-поддержки необходимо дать согласие на обработку персональных данных.\n\n"
    "📋 СОГЛАСИЕ НА ОБРАБОТКУ ПЕРСОНАЛЬНЫХ ДАННЫХ\n\n"
    "Я даю своё согласие на обработку моих персональных данных (ФИО, номер телефона, отдел, описание обращения) "
    "в целях предоставления услуг ИТ-поддержки.\n\n"
    "Обработка включает: сбор, запись, систематизацию, хранение и использование данных. "
    "Данные не передаются третьим лицам без моего согласия.\n\n"
    "Согласие может быть отозвано в любой момент."
