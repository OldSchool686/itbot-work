CONSENT_WITHDRAWN = (
    "Вы отозвали согласие на обработку персональных данных.\n\n"
    "Доступ к боту закрыт."
)


def consent_message(full_name: str = "") -> str:
    """Build full consent message with optional name greeting."""
    greeting = f"Здравствуйте, {full_name}!" if full_name else "Здравствуйте!"
    return f"{greeting}\n\n{CONSENT_BODY}"


def consent_welcome(is_new: bool) -> str:
    """Welcome + consent for new users not in whitelist."""
    greeting = "🎉 Добро пожаловать!" if is_new else "Здравствуйте!"
    return (
        f"{greeting}\n\n"
        "Добро пожаловать в чат-бот техподдержки ИТ-отдела!\n\n"
        "Я помогу вам:\n"
        "• Создать заявку ИТ-поддержки\n"
        "• Найти ответы в базе знаний (AI)\n"
        "• Отследить статус ваших заявок\n\n"
        "Для начала необходимо дать согласие на обработку персональных данных:"
    )
